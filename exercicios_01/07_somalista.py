# Programa para Calcular a Soma Total de uma Lista de Números
# Este programa cria uma lista com números e usa um laço for para somar todos eles.

# Criando uma lista de números
numeros = [5, 8, 12, 20, 7]

# Inicializando a variável para armazenar a soma total
soma_total = 0

# Usando um laço for para percorrer a lista e somar os números
for numero in numeros:
    soma_total += numero

# Exibindo a soma total na tela
print("A soma total dos números é:", soma_total)

