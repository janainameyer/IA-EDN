'''1 - Crie um programa em Python que peça dois números ao usuário, some esses 
valores e exiba o resultado na tela.'''

# Pedindo ao usuário o primeiro número e convertendo para inteiro
num1 = int(input("Digite o primeiro número: "))

# Pedindo ao usuário o segundo número e convertendo para inteiro
num2 = int(input("Digite o segundo número: "))

# Calculando a soma dos dois números
soma = num1 + num2

# Exibindo o resultado na tela
print("A soma dos dois números é:", soma)

