# Programa de Contagem Regressiva
# Este programa faz uma contagem regressiva de 5 até 1 usando um laço while e exibe "Lançar!" no final.

# Inicializando o contador com 5
contador = 5

# Usando um laço while para contar de 5 até 1
while contador > 0:
    print(contador)
    contador -= 1  # Decrementando o contador a cada repetição

# Exibindo a mensagem final
print("Lançar!")

